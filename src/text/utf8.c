#define WIDE_TBL_PAIRS (sizeof(wide_tbl) / sizeof(wide_tbl[0]) / 2)

static int in_range_table(uint32_t cp)
{
	int lo = 0;
	int hi = (int)WIDE_TBL_PAIRS - 1;

	while (lo <= hi) {
		int mid = (lo + hi) / 2;
		uint32_t r_lo = wide_tbl[mid * 2];
		uint32_t r_hi = wide_tbl[mid * 2 + 1];

		if (cp < r_lo)
			hi = mid - 1;
		else if (cp > r_hi)
			lo = mid + 1;
		else
			return 1;
	}
	return 0;
}

