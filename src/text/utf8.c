int cp_display_width(uint32_t cp)
{
	if (cp == 0 || cp == 0x200B || cp == 0xFEFF || cp == 0x2060)
		return 0;
	if (cp < 0x20)
		return 0;
	if (cp < 0x7F)
		return 1;
	if (cp >= 0x7F && cp < 0xA0)
		return 0;
	if (cp == 0x00AD)
		return 0;
	if (cp >= 0x3000 && cp <= 0x3000)
		return 2;

	if (in_range_table(cp))
		return 2;
	return 1;
}

